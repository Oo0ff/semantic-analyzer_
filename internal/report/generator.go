// GenerateReport generates a human-readable report
func (g *Generator) GenerateReport(result *domain.AnalysisResult) ([]byte, error) {
    // Prepare data for template
    data := g.prepareReportData(result)
    
    // Get appropriate template
    templateKey := g.config.Format
    if templateKey == "" {
        templateKey = "text"
    }
    
    tmpl, exists := g.templates[templateKey]
    if !exists {
        return nil, fmt.Errorf("no template for format: %s", templateKey)
    }
    
    // Execute template
    var buf bytes.Buffer
    if err := tmpl.Execute(&buf, data); err != nil {
        return nil, fmt.Errorf("failed to execute template: %w", err)
    }
    
    return buf.Bytes(), nil
}

// prepareReportData prepares data for the report template
func (g *Generator) prepareReportData(result *domain.AnalysisResult) map[string]interface{} {
    data := make(map[string]interface{})
    
    // Basic information
    data["Timestamp"] = result.Timestamp
    data["Version"] = result.Version
    data["ConfigHash"] = result.ConfigHash
    
    // Transcript information
    if result.Transcript != nil {
        data["Transcript"] = map[string]interface{}{
            "Source":    result.Transcript.Source,
            "WordCount": result.Transcript.TokenCount,
            "Sentences": len(result.Transcript.Sentences),
        }
    }
    
    // Statistics
    data["Statistics"] = result.Statistics
    
    // Prepare markers for display
    markers := g.prepareMarkersForDisplay(result.Markers)
    data["Markers"] = markers
    
    // Group markers by level if requested
    if g.config.GroupByLevel {
        markersByLevel := make(map[int][]domain.Marker)
        for _, marker := range markers {
            markersByLevel[marker.Level] = append(markersByLevel[marker.Level], marker)
        }
        data["MarkersByLevel"] = markersByLevel
    }
    
    // Audit summary
    if result.AuditTrail != nil {
        auditSummary := g.prepareAuditSummary(result.AuditTrail)
        data["AuditSummary"] = auditSummary
    }
    
    // Config settings for template
    data["ShowConfidence"] = g.config.ShowConfidence
    data["ShowPositions"] = g.config.ShowPositions
    
    return data
}

// prepareMarkersForDisplay prepares markers for display in report
func (g *Generator) prepareMarkersForDisplay(markers []domain.Marker) []domain.Marker {
    // Sort markers
    sorted := make([]domain.Marker, len(markers))
    copy(sorted, markers)
    g.sortMarkers(sorted)
    
    // Limit number of markers if specified
    if g.config.MaxMarkers > 0 && len(sorted) > g.config.MaxMarkers {
        sorted = sorted[:g.config.MaxMarkers]
    }
    
    return sorted
}

// sortMarkers sorts markers according to configuration
func (g *Generator) sortMarkers(markers []domain.Marker) {
    sortBy := g.config.SortBy
    if sortBy == "" {
        sortBy = "confidence"
    }
    
    // Simple bubble sort for clarity
    for i := 0; i < len(markers); i++ {
        for j := i + 1; j < len(markers); j++ {
            shouldSwap := false
            
            switch sortBy {
            case "confidence":
                shouldSwap = markers[i].Confidence < markers[j].Confidence
            case "level":
                shouldSwap = markers[i].Level < markers[j].Level
            case "type":
                shouldSwap = markers[i].Type > markers[j].Type
            case "position":
                shouldSwap = markers[i].Start > markers[j].Start
            }
            
            if shouldSwap {
                markers[i], markers[j] = markers[j], markers[i]
            }
        }
    }
}

// prepareAuditSummary prepares audit trail summary
func (g *Generator) prepareAuditSummary(auditTrail *domain.AuditTrail) map[string]interface{} {
    if auditTrail == nil {
        return nil
    }
    
    stageCount := make(map[string]int)
    errorCount := 0
    
    for _, event := range auditTrail.Events {
        stageCount[event.Stage]++
        if event.Error != "" {
            errorCount++
        }
    }
    
    return map[string]interface{}{
        "TotalEvents": len(auditTrail.Events),
        "StageCount":  len(stageCount),
        "Errors":      errorCount,
        "StartTime":   auditTrail.StartTime.Format("15:04:05"),
        "EndTime":     auditTrail.EndTime.Format("15:04:05"),
        "Duration":    auditTrail.EndTime.Sub(auditTrail.StartTime).String(),
    }
}

// GenerateSummary generates a brief summary report
func (g *Generator) GenerateSummary(result *domain.AnalysisResult) string {
    var summary strings.Builder
    
    summary.WriteString(fmt.Sprintf("Analysis Summary for: %s\n", result.Transcript.Source))
    summary.WriteString(fmt.Sprintf("Total Markers: %d\n", result.Statistics.TotalMarkers))
    summary.WriteString(fmt.Sprintf("Atomic: %d, Composite: %d\n", 
        result.Statistics.AtomicMarkers, result.Statistics.CompositeMarkers))
    summary.WriteString(fmt.Sprintf("Average Confidence: %.2f\n", result.Statistics.AverageConfidence))
    
    // Top 3 marker types
    if len(result.Statistics.MarkerTypes) > 0 {
        summary.WriteString("Top Marker Types:\n")
        count := 0
        for t, c := range result.Statistics.MarkerTypes {
            summary.WriteString(fmt.Sprintf("  %s: %d\n", t, c))
            count++
            if count >= 3 {
                break
            }
        }
    }
    
    // Processing time
    summary.WriteString(fmt.Sprintf("Processing Time: %.2fs\n", result.Statistics.ProcessingTime))
    
    return summary.String()
}

