// Generator creates human-readable reports
type Generator struct {
    templates   map[string]*template.Template
    config      ReportConfig
}

// ReportConfig holds report generation configuration
type ReportConfig struct {
    Format        string            `json:"format" yaml:"format"` // "text", "html", "markdown"
    TemplatePath  string            `json:"template_path" yaml:"template_path"`
    IncludeSections []string        `json:"include_sections" yaml:"include_sections"`
    Style         map[string]string `json:"style" yaml:"style"`
    MaxMarkers    int               `json:"max_markers" yaml:"max_markers"`
    ShowConfidence bool             `json:"show_confidence" yaml:"show_confidence"`
    ShowPositions  bool             `json:"show_positions" yaml:"show_positions"`
    GroupByLevel   bool             `json:"group_by_level" yaml:"group_by_level"`
    SortBy         string           `json:"sort_by" yaml:"sort_by"` // "confidence", "level", "type", "position"
}

// NewGenerator creates a new report generator
func NewGenerator(cfg ReportConfig) *Generator {
    g := &Generator{
        templates: make(map[string]*template.Template),
        config:    cfg,
    }
    
    g.initialize()
    return g
}

// initialize sets up the report generator
func (g *Generator) initialize() {
    // Load default templates
    g.loadDefaultTemplates()
    
    // Load custom template if specified
    if g.config.TemplatePath != "" {
        g.loadCustomTemplate()
    }
}

